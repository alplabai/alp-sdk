"""Generate the canonical test fixture: a minimal .alpmodel + its C byte array.
Run as: python -m alp_model._gen_fixture   (writes both committed files)."""
from __future__ import annotations
from pathlib import Path
from .manifest import Manifest, Tensor, Target, Coverage
from .package import write_package

_ROOT = Path(__file__).resolve().parents[2]
_ALPMODEL = _ROOT / "tests/fixtures/alpmodel/minimal.alpmodel"
_HEADER = _ROOT / "tests/unit/alpmodel_reader/src/fixture.h"


def _manifest() -> Manifest:
    return Manifest(
        name="minimal",
        src_sha=bytes(range(32)),
        inputs=[Tensor("int8", 4, [1, 96, 96, 1], 0.0078, -1)],
        outputs=[Tensor("int8", 2, [1, 2], 0.004, 0)],
        targets=[
            Target("ethos_u", "alif:ensemble:e8", "vela_tflite", "ethos-u85-256",
                   65536, {"sram_kib": 256, "op_features": []}, 0, compiler_version="vela 4.1.0"),
            Target("cpu", "*", "tflite", "", 131072,
                   {"sram_kib": 0, "op_features": []}, 1, compiler_version="passthrough"),
        ],
        coverage=[Coverage("deepx_dxm1", "", "skipped", "dx-compiler not found")],
    )


def build_fixture_bytes() -> bytes:
    return write_package(_manifest(), [b"VELA\x00\x01\x02\x03", b"TFLITE\x00"])


def to_c_header(raw: bytes) -> str:
    lines = ["/* GENERATED by `python -m alp_model._gen_fixture` -- do not edit. */",
             "#ifndef ALP_MODEL_FIXTURE_H", "#define ALP_MODEL_FIXTURE_H",
             "#include <stddef.h>", "#include <stdint.h>", "",
             "static const uint8_t alp_model_fixture[] = {"]
    for i in range(0, len(raw), 12):
        chunk = ", ".join(f"0x{b:02x}" for b in raw[i:i + 12])
        lines.append(f"    {chunk},")
    lines += ["};",
              f"static const size_t alp_model_fixture_len = {len(raw)};",
              "", "#endif /* ALP_MODEL_FIXTURE_H */", ""]
    return "\n".join(lines)


def main() -> None:
    raw = build_fixture_bytes()
    _ALPMODEL.parent.mkdir(parents=True, exist_ok=True)
    _HEADER.parent.mkdir(parents=True, exist_ok=True)
    _ALPMODEL.write_bytes(raw)
    _HEADER.write_text(to_c_header(raw))
    print(f"wrote {_ALPMODEL} ({len(raw)} bytes) and {_HEADER}")


if __name__ == "__main__":
    main()
