"""
`west alp-build` -- the one-command developer experience for alp-sdk apps.

Customers run:

    west alp-build -b native_sim/native/64 examples/gpio-button-led

Under the hood the command runs the alp-sdk pre-flight + delegates
to Zephyr's builder:

  1. Validates `<app_path>/board.yaml` against the schema +
     hw_rev compatibility window (scripts/validate_board_yaml.py).
     Exit code 1/2/3 aborts the build before any compile work.
  2. Pre-generates `<build>/generated/alp_hw_info_build.h` so apps
     that include it pick up the build-time ALP_HW_BUILD_* macros.
     (The companion alp.conf is auto-generated by the app's
     CMakeLists.txt; this only covers the side-channel header.)
  3. Sets EXTRA_ZEPHYR_MODULES + ALP_SDK_ROOT so the application's
     CMakeLists.txt can resolve the SDK without per-customer
     overrides.
  4. Invokes `west build` with the original board + app args.
     Any extra args after the recognised flags pass through
     verbatim.

Surfaced as a Zephyr west-extension command via
`scripts/west-commands.yml` referenced from `zephyr/module.yml` --
customers who import alp-sdk into their Zephyr workspace get the
command automatically.
"""

from __future__ import annotations

import argparse
import os
import subprocess
import sys
from pathlib import Path
from typing import Optional

from west import log  # type: ignore[import-not-found]
from west.commands import WestCommand  # type: ignore[import-not-found]


class AlpBuild(WestCommand):

    def __init__(self) -> None:
        super().__init__(
            "alp-build",
            "Validate board.yaml + generate config + build an alp-sdk app",
            "\n".join(__doc__.splitlines()[2:]) if __doc__ else "",
        )

    def do_add_parser(self, parser_adder):  # type: ignore[no-untyped-def]
        parser = parser_adder.add_parser(
            self.name,
            help=self.help,
            description=self.description,
            formatter_class=argparse.RawDescriptionHelpFormatter,
        )
        parser.add_argument(
            "-b", "--board", required=True,
            help="Zephyr board target (e.g. native_sim/native/64)")
        parser.add_argument(
            "app_path",
            help="Path to the application source directory")
        parser.add_argument(
            "--board-yaml", default=None,
            help="Override path to board.yaml "
                 "(default: <app_path>/board.yaml)")
        parser.add_argument(
            "--no-validate", action="store_true",
            help="Skip the validate_board_yaml.py pre-flight check.")
        parser.add_argument(
            "--skip-hw-info-h", action="store_true",
            help="Skip pre-generating <alp_hw_info_build.h>.")
        return parser

    def do_run(self, args, west_build_args):  # type: ignore[no-untyped-def]
        sdk_root = self._sdk_root()
        if sdk_root is None:
            log.die(
                "Cannot locate alp-sdk root.  Set ALP_SDK_ROOT or import "
                "alp-sdk as a Zephyr module so the command can resolve "
                "scripts/alp_project.py.")
            return 1
        log.dbg(f"alp-build: SDK root = {sdk_root}")

        app_path = Path(args.app_path).resolve()
        if not app_path.is_dir():
            log.die(f"App path not found: {app_path}")
            return 1

        board_yaml = (Path(args.board_yaml).resolve()
                      if args.board_yaml
                      else app_path / "board.yaml")
        if not board_yaml.is_file():
            log.die(f"board.yaml not found at {board_yaml}.")
            return 1

        python = sys.executable or "python3"

        # Step 1 -- validate.
        if not args.no_validate:
            log.inf(f"alp-build: validating {board_yaml}")
            rv = subprocess.call([
                python,
                str(sdk_root / "scripts" / "validate_board_yaml.py"),
                "--input", str(board_yaml),
            ])
            if rv != 0:
                log.die(f"validate_board_yaml.py failed (rv={rv})")
                return rv

        # Step 2 -- pre-generate hw-info-h.  alp.conf is auto-emitted
        # by the example's CMakeLists.txt; this header is the
        # side-channel apps include alongside <alp/hw_info.h>.
        if not args.skip_hw_info_h:
            out = app_path / "build" / "generated" / "alp_hw_info_build.h"
            out.parent.mkdir(parents=True, exist_ok=True)
            log.inf(f"alp-build: generating {out.relative_to(app_path)}")
            rv = subprocess.call([
                python,
                str(sdk_root / "scripts" / "alp_project.py"),
                "--input", str(board_yaml),
                "--emit", "hw-info-h",
                "--output", str(out),
            ])
            if rv != 0:
                log.die(f"alp_project.py hw-info-h failed (rv={rv})")
                return rv

        # Step 3 -- env wiring + delegate to west build.
        env = os.environ.copy()
        existing = env.get("EXTRA_ZEPHYR_MODULES", "")
        sep = os.pathsep
        if str(sdk_root) not in existing.split(sep):
            env["EXTRA_ZEPHYR_MODULES"] = (existing + sep + str(sdk_root)
                                            if existing else str(sdk_root))
        env["ALP_SDK_ROOT"] = str(sdk_root)

        cmd = ["west", "build", "-b", args.board, str(app_path)] + list(west_build_args)
        log.inf(f"alp-build: $ {' '.join(cmd)}")
        return subprocess.call(cmd, env=env)

    @staticmethod
    def _sdk_root() -> Optional[Path]:
        """Locate the alp-sdk root through (in order):
        ALP_SDK_ROOT env, this file's grandparent, EXTRA_ZEPHYR_MODULES."""
        env_root = os.environ.get("ALP_SDK_ROOT", "").strip()
        if env_root:
            p = Path(env_root)
            if (p / "scripts" / "alp_project.py").is_file():
                return p

        # scripts/west_commands/alp.py -> the sdk root is two parents up.
        candidate = Path(__file__).resolve().parents[2]
        if (candidate / "scripts" / "alp_project.py").is_file():
            return candidate

        for var in ("EXTRA_ZEPHYR_MODULES", "ZEPHYR_EXTRA_MODULES"):
            for entry in os.environ.get(var, "").split(os.pathsep):
                if entry and (Path(entry) / "scripts" / "alp_project.py").is_file():
                    return Path(entry)
        return None
