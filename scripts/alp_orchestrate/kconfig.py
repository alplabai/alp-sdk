"""Per-slice Kconfig (alp.conf) emitter.

`_slice_alp_conf` renders a Zephyr slice's Kconfig fragment off the parsed model
+ the resolved capabilities / chip-driver / library / peripheral enables;
`_emit_extra_library_profile` renders an extra `libraries:` profile fragment.
Extracted as the #285 kconfig emit seam. The slug / peripheral-Kconfig tables
come from the slugs.py leaf; the `_CHIP_SUBSYSTEMS` table is lazy-imported from
alp_project inside the function (the existing alp_project<->alp_orchestrate
cycle-break), with the same in-body sys.path setup. Per-core `libraries:`
Kconfig is read from each library's manifest via `_per_core_library_kconfig`.

`_slice_alp_conf` itself is a thin composer over a set of `_emit_*` section
helpers (console / SoM capability / chip driver / subsystem / library /
inference / memory / power / storage / OTA / diagnostics), each returning
that section's `list[str]` fragment. Split out mechanically as the #673
Phase-1 kconfig split -- behavior-preserving, byte-identical emit output;
see `check_emit_snapshots.py`.

This module is a pure string templater: it never runs kconfiglib/west, so
every emitted `CONFIG_*` line must be provable correct from this module's own
YAML/JSON inputs + its own `zephyr/kconfigs/*.kconfig` tree -- it cannot ask
the real Kconfig solver whether a symbol's dependency chain resolves.
Consequently the emitted alp.conf's Kconfig symbol set is coupled to the
alp-sdk module version that generated it, the same way `west.yml` deliberately
pins one Zephyr/module release at a time (docs/zephyr-version-policy.md); an
alp.conf generated by one alp-sdk version is not a promised-compatible input
to a Zephyr tree built against another. Detecting that cross-version skew
cleanly (rather than a Kconfig "assign to undefined symbol" abort) is #855's
job, not this module's -- this module only owns intra-version dependency
gating (see #874).
"""

